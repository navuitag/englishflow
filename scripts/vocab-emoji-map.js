export function slugifyVocabKey(en) {
  return String(en)
    .toLowerCase()
    .trim()
    .replace(/['']/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

export function lookupVocabEmoji(en) {
  const key = String(en).toLowerCase().trim();
  if (EXACT[key]) return EXACT[key];
  for (const [pattern, emoji] of RULES) {
    if (pattern.test(key)) return emoji;
  }
  return "📖";
}
